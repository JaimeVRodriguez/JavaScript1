// Reference Types
// Objects, Arrays, Functions

// When you have variables that are all in the same "group" together
let person = {
    name: 'Jaime',
    age: 30
};

console.log(person)

// Dot notation
person.name = 'Gunner';
console.log(person.name)

// Bracket Notation
person['name'] = 'Krystal'
console.log(person.name)